    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        // Code for submit
        try
        {
            String rno=jTextField1.getText();
            String name=jTextField2.getText();
            String course=jTextField3.getText();
            String sem=jTextField4.getText();
            String addr=jTextField5.getText();
            String cont=jTextField6.getText();
            String mail=jTextField7.getText();
            DB.DBCon db=new DB.DBCon();
            db.pstmt=db.con.prepareStatement("insert into student_info values(?,?,?,?,?,?,?)");
            db.pstmt.setString(1,rno);
            db.pstmt.setString(2,name);
            db.pstmt.setString(3,course);
            db.pstmt.setString(4,sem);
            db.pstmt.setString(5,addr);
            db.pstmt.setString(6,cont);
            db.pstmt.setString(7,mail);
            int i=db.pstmt.executeUpdate();
            if(i>0)
            {
                javax.swing.JOptionPane.showMessageDialog(this,"Student Registered Successfully");
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
                jTextField5.setText("");
                jTextField6.setText("");
                jTextField7.setText("");
            }
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton9ActionPerformed

